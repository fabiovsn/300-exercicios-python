# Operações matemáticas com entrada de dados

num1 = int(input("Digite um número:\n"))
num2 = int(input("Digite outro número:\n"))

print(f"A soma de {num1} e {num2} é: {num1 + num2}\n")
print(f"A subtração de {num1} e {num2} é: {num1 - num2}\n")
print(f"A multiplicação de {num1} e {num2} é: {num1 * num2}\n")
print(f"A divisão de {num1} e {num2} é: {num1 / num2}\n")